  if (newVal !== oldVal) {
    addMessageToAtendente(newVal, isChatOpen.value, selectedAtendente.value?.id);
  }
});

watch(isChatOpen, (newVal) => {
  if (newVal) {
    setTimeout(() => {
      showContent.value = true;
    }, 400);
  }
});
