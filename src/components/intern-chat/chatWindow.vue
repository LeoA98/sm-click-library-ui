  fetchAtendentes,
  fetchMessagesForAtendente,
  addMessageToAtendente,
  hasNextPageForAtendente,
  sendMessageToAtendente,
  loadMessagesForAtendente,
  resetUnreadMessages
} = useChat();

const isChatOpen = ref(false);
const showContent = ref(false);
const selectedAtendente = ref(null);

// Computed property para obter o n√∫mero de mensagens n√£o lidas
const unreadMessagesCount = computed(() => {
  if (selectedAtendente.value) {
    const atendente = attendants.value.find(att => att.id === selectedAtendente.value.id);
    return atendente ? atendente.unreadMessages : 0;
  }
  return 0;
});

onMounted(async () => {
  await fetchAtendentes(props.token, props.get_attendants);  // Carrega os atendentes
});

const toggleChat = () => {
  isChatOpen.value = !isChatOpen.value;
  if (isChatOpen.value && selectedAtendente.value) {
    resetUnreadMessages(selectedAtendente.value.id); // Reseta as mensagens n√£o lidas ao abrir o chat
  }
};

const handleChatClick = () => {
  if (!isChatOpen.value) toggleChat();
};

const selecionarAtendente = async (atendente) => {
  selectedAtendente.value = atendente;
  resetUnreadMessages(atendente.id); // Reseta as mensagens n√£o lidas ao selecionar o atendente
  if (!atendente.messages) {
    await fetchMessagesForAtendente(atendente.id, props.token, props.get_internal_chat);
  }
};

